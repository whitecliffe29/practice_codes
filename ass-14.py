# WAP to input two number and check whether both numbers are equal or not

n1 = int(input("Enter 1st number : "))
n2 = int(input("Enter 2nd number : "))

if n1 == n2:
    print("{} and {} are equal.".format(n1, n2))
else:
    print("{} and {} are not equal.".format(n1, n2))
