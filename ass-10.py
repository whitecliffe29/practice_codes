# WAP to input a number in decimal form and display it in binary form, in octal form and in hexadecimal form

dec = int(input("Enter a decimal number : "))

print("Decimal to Binary is {}".format(bin(dec)))
print("Decimal to Octal is {}".format(oct(dec)))
print("Decimal to Hexadecimal is {}".format(hex(dec)))
